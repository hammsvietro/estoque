    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Arquivo.lerProdutos();
        int achou=0;
        if(jbUser.getText().isEmpty() || jbPassword.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha todos os campos");
        }else{
            for(Usuario user: Facade.getUsuarios()){
                if(user.getUserName().equals(jbUser.getText()) && user.getSenha().equals(jbPassword.getText())){
                    achou=1;
                    if(user instanceof Admin){
                        new MainTelaAdmin().setVisible(true);
                        dispose();
                    }else{
                        new MainTelaUser().setVisible(true);
                        dispose();
                        }
                }
            }
            if(achou == 0)
                JOptionPane.showMessageDialog(null, "Usuario nao encontrado!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

